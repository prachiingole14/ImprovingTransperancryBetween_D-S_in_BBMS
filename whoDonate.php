        <p>In India, eligibility criteria for blood donation are typically governed by the guidelines set by the National Blood 
            Transfusion Council (NBTC) and individual blood banks. The eligibility criteria may vary slightly from one blood bank to 
            another, but generally, the following are the common eligibility requirements for blood donation in India:</p><br>

        <h4>1. Age:</h4>
        <p>Typically, Donors should be between 18 and 65 years old. However, some blood banks may have slightly different age limits.</p>
        <br>

        <h4>2. Weight:</h4>
        <p>Donors should weigh at least 45 kg (99 pounds) to donate blood.</p>
        <br>

        <h4>3. Health:</h4>
        <p>Donors should be in good health on the day of donation. They should not have any acute or chronic illnesses or infections.</p>
        <br>

        <h4>4. Hemoglobin Level:</h4>
        <p> The minimum hemoglobin level required for blood donation in India is usually 12.5 g/dL or above.</p>
        <br>

        <h4>5. Pulse Rate:</h4>
        <p> The pulse rate should be within a specific range, often between 50 to 100 beats per minute.</p>
        <br>

        <h4>6. Blood Pressure:</h4>
        <p>Blood pressure should typically be within the normal range, although the specific values may vary slightly among blood banks.</p>
        <br>

        <h4>7. Medical History:</h4>
        <p> Donors are asked about their medical history, including any history of diseases like HIV/AIDS, hepatitis, or malaria. 
            Some medical conditions or medications may disqualify a person from donating.</p>
        <br>

        <h4>8. Travel History:</h4>
        <p>Recent travel to certain regions with a high prevalence of infectious diseases, such as malaria or Zika, may disqualify a person from donating.</p>
        <br>

        <h4>9. Pregnancy:</h4>
        <p> Pregnant women are usually not eligible to donate blood.</p>
        <br>

        <h4>10. High-Risk Behaviors: </h4>
        <p> People engaging in high-risk behaviors, such as intravenous drug use or unprotected sex, may be deferred from donating.</p>
        <br>

        <h4>11. Tattoos and Piercings:</h4>
        <p> Depending on local guidelines, individuals with recent tattoos or piercings may have to wait for a specified period before donating blood.</p>
        <br>

        <h4>12. Vaccinations: </h4>
        <p>Recent vaccinations may also impact eligibility, and donors may be deferred for a certain period after receiving certain vaccines.</p>
